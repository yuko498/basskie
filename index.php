  $(document).ready(function(){
var quickNav = document.querySelector('.quickNav');
var items = quickNav.querySelectorAll('.quickNav__item');

var closeAll = function closeAll() {

  var activeMenus = quickNav.querySelectorAll('.quickNav__menu.active');
  var activeItems = quickNav.querySelectorAll('.quickNav__item.active');

  quickNav.classList.remove('active');
  Array.prototype.forEach.call(activeItems, function (activeItem) {return activeItem.classList.remove('active');});
  Array.prototype.forEach.call(activeMenus, function (activeMenu) {return activeMenu.classList.remove('active');});

};

quickNav.onclick = closeAll;

Array.prototype.forEach.call(items, function (item) {

  item.onclick = function (e) {

    e.stopPropagation();
    e.preventDefault();

    var href = item.getAttribute('href');
    var newMenu = quickNav.querySelector(href);
    var isActive = newMenu.classList.contains('active');

    closeAll();

    if (isActive === false) {
      quickNav.classList.add('active');
      newMenu.classList.add('active');
      item.classList.add('active');
    }

  };

});

  });



});
